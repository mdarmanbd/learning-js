let date = newDate.getDate();
console.log(date); 

let day = newDate.getDay();
console.log(day); // day also start is 0 to 6;

let hours = newDate.getHours();
console.log(hours);

let min = newDate.getMinutes();
console.log(min);

let sec = newDate.getSeconds();
console.log(sec);